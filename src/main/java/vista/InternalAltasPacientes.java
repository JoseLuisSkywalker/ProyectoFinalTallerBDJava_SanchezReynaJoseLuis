    private void btnAgregarPacientesAltasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarPacientesAltasActionPerformed
        // TODO add your handling code here:
       if (campoTelefonoPacientesAltas.getText().length() != 10) {
        JOptionPane.showMessageDialog(this, "El número dene ser de 10 digitos ");
        
        campoTelefonoPacientesAltas.requestFocus(); //este metodo hace que no se vaya el resultado erroneo para el tel.
        return; 
    } 
        
        Paciente p = new Paciente(); 
        
        p.setIdPaciente(Integer.parseInt(campoIDPacienteAltas.getText().trim()));
        p.setNombre(campoNombrePacientesAltas.getText().trim());
        p.setApellido(campoApellidoPacientesAltas.getText().trim());
        p.setTelefono(campoTelefonoPacientesAltas.getText().trim()); 
        String diaNac  = comboDiaNacAltas.getSelectedItem().toString();
        String mesNac  = comboMesNacAltas.getSelectedItem().toString();
        String anioNac = comboAnoNacAltas.getSelectedItem().toString();
        String fechaNacimiento = anioNac + "-" + mesNac + "-" + diaNac;
        p.setFechaNacimiento(fechaNacimiento); 
        p.setSexo(comboSexoAltas.getSelectedItem().toString());
        p.setEstadoCivil(comboCivilAltas.getSelectedItem().toString());
        String diaReg  = comboDiaRegistroAltas.getSelectedItem().toString();
        String mesReg  = comboMesRegistroAltas.getSelectedItem().toString();
        String anioReg = comboAnoRegistroAltas.getSelectedItem().toString();
        String fechaRegistro = anioReg + "-" + mesReg + "-" + diaReg;
        p.setFechaRegistro(fechaRegistro);
        int idMedicoCabecera = Integer.parseInt(campoIdMedicosAltas.getText().trim());
        p.setIdMedico(idMedicoCabecera);
        
        
        boolean ok = false; 
        try {
            ok = PacienteDAO.getInstancia().agregarPaciente(p);
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
        
        if(ok){
            //                                                      Aqui es donde plasmo el int edad_paciente del Funcion 2 
            //                                                       ||||||||||||||||||||||
            //                                                       vvvvvvvvvvvvvvvvvvvvvv
                JOptionPane.showMessageDialog(this, "Paciente de " + PacienteDAO.getInstancia().obtenerEdadPaciente(p.getIdPaciente()) + " años de edad registrado exitosamente.");
                restablecerCampos();
            }else{
                JOptionPane.showMessageDialog(this, "Paciente no se puedo registrar correctamente: verifique el ID para paciente como también a médicos.");
            }
        
        cargarTodosLosPacientes(tablaAltasPacientes);
        
        ConexionBD con = new ConexionBD(); 
        PreparedStatement ps2 = null;

        try {
            con.abrirConexion(); 
            String sqlUpdate = "UPDATE estadisticas SET total_pacientes = total_pacientes + 1 WHERE id = 1";
            ps2 = con.getConexion().prepareStatement(sqlUpdate);
            ps2.executeUpdate();
            
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            if (ps2 != null) try { ps2.close(); } catch (Exception ex) {}
            con.cerrarConexion();
        }       
        actualizarLabelPacientes();
        
    }//GEN-LAST:event_btnAgregarPacientesAltasActionPerformed
        
